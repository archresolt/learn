package mychain;

public class Car {
private int carNo;
private String brand;
private int carSize;

public int getCarNo(int carNo){
	return carNo;
	
}
public void setCarNo(int carNo){
	this.carNo = carNo;
}
public String getBrand(String brand){
	return brand;
}
public void setBrand(String brand){
	this.brand = brand;
}
public int getCarSize(int carSize){
	return carSize;
}
public void setCarSize(int carSize){
	this.carSize = carSize;
}
}

