package Text1;

public abstract class Animal {
	

	private int age;
	
	
	public Animal() {
		super();
		// TODO 自动生成的构造函数存根
	}
	
	public Animal(int age) {
		super();
		this.age = age;
	}



	public abstract void show();

	
	
	

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	
	
		
	
		
}

	